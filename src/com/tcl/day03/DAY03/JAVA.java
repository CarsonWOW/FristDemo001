package com.tcl.day03.DAY03;

public class JAVA {
    //变量
    //主程序入口
    public static void main(String[] args) {
        //定义变量第一种方式
        //1.先定义，先声明，不赋值 不可以直接访问 会报错
        //int,数据类型 num为变量名，可以随便取，但必须为整数数据
        int num;
        //在赋值
         num=100;
         //向控制台输出信息
        System.out.println("值为"+num);
        //定义变量第二种方式
        //直接声明并赋值
        int money=1000;
        System.out.println(money);

    }
}
